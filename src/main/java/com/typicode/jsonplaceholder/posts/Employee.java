package com.typicode.jsonplaceholder.posts;

public class Employee {
    private String name;
    private String surname;
    private int userId;
    private int id;
    public Employee(){}

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getUserId() {
        return userId;
    }

    public int getId() {
        return id;
    }
}
